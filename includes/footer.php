<!-- Core Web Vitals Monitoring -->
<script>
    function sendVitalsToAnalytics(metric, value) {
        // Replace with your analytics endpoint
        if (navigator.sendBeacon) {
            navigator.sendBeacon('/api/vitals', JSON.stringify({ metric, value, ts: Date.now(), url: location.pathname }));
        }
    }

    // LCP
    new PerformanceObserver((list) => {
        const entries = list.getEntries();
        const last = entries[entries.length - 1];
        sendVitalsToAnalytics('LCP', Math.round(last.startTime));
    }).observe({ type: 'largest-contentful-paint', buffered: true });

    // CLS
    let cumulativeCLS = 0;
    new PerformanceObserver((list) => {
        for (const entry of list.getEntries()) {
            if (!entry.hadRecentInput) cumulativeCLS += entry.value;
        }
        sendVitalsToAnalytics('CLS', Math.round(cumulativeCLS * 1000) / 1000);
    }).observe({ type: 'layout-shift', buffered: true });

    // INP (Interaction to Next Paint)
    new PerformanceObserver((list) => {
        for (const entry of list.getEntries()) {
            const inp = entry.processingEnd - entry.processingStart;
            sendVitalsToAnalytics('INP', Math.round(inp));
        }
    }).observe({ type: 'event', buffered: true, durationThreshold: 40 });
</script>

<!-- Global Cart System JS -->
<script>
    function getCart() {
        return JSON.parse(localStorage.getItem('readymart_cart')) || [];
    }

    function saveCart(cart) {
        localStorage.setItem('readymart_cart', JSON.stringify(cart));
        updateHeaderCart();
    }

    function addToCart(id, name, price, image, qty = 1, redirect = false) {
        let cart = getCart();
        let existing = cart.find(item => item.id === id);
        
        if (existing) {
            existing.qty += qty;
        } else {
            cart.push({ id, name, price, image, qty });
        }
        
        saveCart(cart);
        
        if (redirect) {
            window.location.href = 'checkout.php';
        } else {
            alert('Item successfully added to your cart!');
        }
    }

    function updateHeaderCart() {
        let cart = getCart();
        let count = 0;
        let total = 0;
        
        cart.forEach(item => {
            count += item.qty;
            total += item.price * item.qty;
        });
        
        const countEl = document.getElementById('header_cart_count');
        const totalEl = document.getElementById('header_cart_total');
        
        if (countEl) countEl.innerText = count;
        if (totalEl) totalEl.innerText = total + ' TK';
    }

    // Initialize the header cart on page load
    document.addEventListener('DOMContentLoaded', updateHeaderCart);
</script>

